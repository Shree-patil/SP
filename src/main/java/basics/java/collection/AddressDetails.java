package basics.java.collection;

/**
 * 
 */
public class AddressDetails  {
	
	private int doorNumber;
	private String appartmentName;
	private String post;
	private String mandal;
	private String district;
	private String state;
	private int pinCode;
	/**
	 * @return the doorNumber
	 */
	public int getDoorNumber() {
		return doorNumber;
	}
	/**
	 * @param doorNumber the doorNumber to set
	 */
	public void setDoorNumber(int doorNumber) {
		this.doorNumber = doorNumber;
	}
	/**
	 * @return the appartmentName
	 */
	public String getAppartmentName() {
		return appartmentName;
	}
	/**
	 * @param appartmentName the appartmentName to set
	 */
	public void setAppartmentName(String appartmentName) {
		this.appartmentName = appartmentName;
	}
	/**
	 * @return the post
	 */
	public String getPost() {
		return post;
	}
	/**
	 * @param post the post to set
	 */
	public void setPost(String post) {
		this.post = post;
	}
	/**
	 * @return the mandal
	 */
	public String getMandal() {
		return mandal;
	}
	/**
	 * @param mandal the mandal to set
	 */
	public void setMandal(String mandal) {
		this.mandal = mandal;
	}
	/**
	 * @return the district
	 */
	public String getDistrict() {
		return district;
	}
	/**
	 * @param district the district to set
	 */
	public void setDistrict(String district) {
		this.district = district;
	}
	/**
	 * @return the state
	 */
	public String getState() {
		return state;
	}
	/**
	 * @param state the state to set
	 */
	public void setState(String state) {
		this.state = state;
	}
	/**
	 * @return the pinCode
	 */
	public int getPinCode() {
		return pinCode;
	}
	/**
	 * @param pinCode the pinCode to set
	 */
	public void setPinCode(int pinCode) {
		this.pinCode = pinCode;
	}
	

}
